# Math Function
#Example No.01

import math

x = 20
print(math.sqrt(x))      # 4.0

print(math.pow(2, 3))    # 8.0

print(math.ceil(4.2))    # 5
print(math.floor(4.7))   # 4

print(math.sin(math.pi/2))  # 1.0
print(math.log(100, 10))    # 2.0

print(math.factorial(5))    # 120
