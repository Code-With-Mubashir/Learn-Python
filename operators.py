# Operators in python
# Arithmetic Operators
# Example No.01
a = 10
b = 20
z = a + b
print(z)

# Example No.02
x = b - a
print(x)
# Example No.03
y = a * b
print(y)
# Example No.04
c = b / a
print(c)
# Example No.05
d = b % a
print(d)
# Example No.06
e = b ** a
print(e)
# Example No.07
f = b // a
print(f)
# Example No.08
g = a + b * a - b / a
print(g)
# Example No.09
h = (a + b) * a - b / a
print(h)

# Example No.10
i = a + (b * a) - (b / a)
print(i)

# 2.Comparison operator
# Example No.01
a = 10
b = 20
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
# Example No.02
x = 5
y = 10
print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)
print(x is y)
print(x is not y)


# Logical operators
# Example No.01
a = 10
